		<h2>Outside Learning Resources</h2>
		<h3>LeetCode / NeetCode</h3>
		<p>
			For interview prep and online assessments (OAs) we recommend
			LeetCode style practice problems. At OSU we prefer the NeetCode
			problem set and interface due to its nature of guiding you through
			each set of topics.
		</p>

		<ul>
			<li><a href="https://leetcode.com/">LeetCode</a></li>
			<li><a href="https://neetcode.io/">NeetCode</a></li>
		</ul>

		<h2>Competitive Programming</h2>

		<ul>
			<li>
				<a href="https://codeforces.com/">Codeforces</a>
			</li>
			<ul>
				<li>
					Most popular online competitive programming platform with
					regular live competitions a ranking system.
				</li>
			</ul>

			<li>
				<a href="https://adventofcode.com/">Advent of Code</a>
			</li>
			<ul>
				<li>
					Lighthearted and fun way to solve coding problems of
					increasing difficulty each day for the first 25 days of
					December!
				</li>
			</ul>

			<li><a href="https://cses.fi/problemset/">CSES Problem Set</a></li>
			<ul>
				<li>
					Online judge containing popular programming problems sorted
					by type. Goes great with Competitive Programmer's Handbook!
				</li>
			</ul>

			<li>
				<a href="https://cses.fi/book/book.pdf"
					>Competitive Programmer's Handbook</a
				>
			</li>
			<ul>
				<li>
					Free book on the fundamentals of competitive programming.
					Pairs well with CSES!
				</li>
			</ul>
			<li>
				<a href="https://projecteuler.net/">Project Euler</a>
			</li>
			<ul>
				<li>
					Online collection of interesting programming/math problems.
				</li>
			</ul>
		</ul>

